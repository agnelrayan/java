public class SwitchEx {
    public static void main(String[] args) {
        int a=1;
        switch (a)
        {
            case 1:
                System.out.println("In Case 1");
                break;
            case 2:
                System.out.println("IN case 2");
                break;
            case 3:
                System.out.println("In case 3");
                break;
            default:
                System.out.println("Choose correct case");
                break;
        }

    }
}
