public class TypeCastEx4 {
    public static strictfp void main(String[] args) {
        float a=10.000f;
        int a2=10;
        long a3=100;
        double a4 = 1000;
        float a5=200f;

        System.out.println(a);
        System.out.println("A2:"+a2);
        System.out.println("A3:"+a3);
        System.out.println("A4:"+a4);
        System.out.println("A5:"+a5);
    }
}
